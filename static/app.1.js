var t2p = function(t) {
  return t * TILE;
};

var p2t = function(p) {
  return Math.floor(p / TILE);
};

const tcell = function(tx, ty) {
  return map.data[tx + ty * MAP.tw];
};

const cell = function(x, y) {
  return tcell(p2t(x), p2t(y));
};

const Game = {
  run: function(options) {
    var now,
      dt = 0,
      last = timestamp(),
      //slow = options.slow || 1,
      slow = 1,
      step = 1 / options.fps,
      slowStep = step * slow,
      update = options.update,
      render = options.render,
      frame = function() {
        now = timestamp();
        dt = dt + Math.min(1, (now - last) / 1000);
        while (dt > slowStep) {
          //skips slow step until dt catches up
          dt = dt - slowStep;
          update(step);
        }
        render(dt / slow);
        last = now;
        requestAnimationFrame(frame);
      };
    requestAnimationFrame(frame);
  }
};

Game.Math = {
  random: function(min, max) {
    return Math.random() * (max - min) + min;
  },
  randomInt: function(min, max) {
    //dont use arrow here as context is
    //bound to to
    return Math.round(this.random(min, max));
  },
  randomChoice: function(choices) {
    return choices[this.randomInt(0, choices.length - 1)];
  },
  randomBool: function() {
    //return this.randomInt(0, 1) === 1 ? true : false;
    return this.randomChoice([true, false]);
  },
  limit: function(x, min, max) {
    return Math.max(min, Math.min(max, x));
  },

  between: function(n, min, max) {
    return n >= min && n <= max;
  },

  accelerate: function(v, accel, dt) {
    return v + accel * dt;
  },

  lerp: function(n, dn, dt) {
    // linear interpolation
    return n + dn * dt;
  }
};

Game.run({
  fps: 60,
  update: dt => {
    player.y = Math.floor(player.y + dt * player.dy);
    player.x = Math.floor(player.x + dt * player.dx);

    var wasleft = player.dx < 0,
      wasright = player.dx > 0,
      falling = player.falling;

    player.ddx = 0;
    player.ddy = GRAVITY;
    //player.ddy = 0;

    if (player.input.left) player.ddx = player.ddx - ACCEL;
    // player wants to go left
    else if (wasleft) player.ddx = player.ddx + FRICTION; // player was going left, but not any more

    if (player.input.right) player.ddx = player.ddx + ACCEL;
    // player wants to go right
    else if (wasright) player.ddx = player.ddx - FRICTION; // player was going right, but not any more

    if (player.input.jump && !player.jumping && !falling) {
      player.ddy = player.ddy - JUMP; // apply an instantaneous (large) vertical impulse
      player.jumping = true;
    }

    //we update the player with the previous vector?? Why not the other way around??

    //calculate the next vector
    player.dx = Game.Math.limit(player.dx + dt * player.ddx, -MAXDX, MAXDX); //we only need 1/60 of the amount of ddx for the frame 60FPS
    player.dy = Game.Math.limit(player.dy + dt * player.ddy, -MAXDY, MAXDY);

    //is this in the correct place?
    if ((wasleft && player.dx > 0) || (wasright && player.dx < 0)) {
      player.dx = 0; // clamp at zero to prevent friction from making us jiggle side to side
    }

    //collision detection

    var tx = p2t(player.x),
      ty = p2t(player.y),
      nx = player.x % TILE, // true if player overlaps right
      ny = player.y % TILE, // true if player overlaps below
      cell = tcell(tx, ty),
      cellright = tcell(tx + 1, ty), //can this return undefined??
      celldown = tcell(tx, ty + 1),
      celldiag = tcell(tx + 1, ty + 1);

    //this works as cells with 0 or undefined evaluate to zero.
    if (player.dy > 0) {
      if ((celldown && !cell) || (celldiag && !cellright && nx)) {
        player.y = t2p(ty); // clamp the y position to avoid falling into platform below
        player.dy = 0; // stop downward velocity
        player.falling = false; // no longer falling
        player.jumping = false; // (or jumping)
        ny = 0; // - no longer overlaps the cells below
      }
    } else if (player.dy < 0) {
      if ((cell && !celldown) || (cellright && !celldiag && nx)) {
        player.y = t2p(ty + 1); // clamp the y position to avoid jumping into platform above
        player.dy = 0; // stop upward velocity
        cell = celldown; // player is no longer really in that cell, we clamped them to the cell below
        cellright = celldiag; // (ditto)
        ny = 0; // player no longer overlaps the cells below
      }
    }

    if (player.dx > 0) {
      if ((cellright && !cell) || (celldiag && !celldown && ny)) {
        player.x = t2p(tx); // clamp the x position to avoid moving into the platform we just hit
        player.dx = 0; // stop horizontal velocity
        nx = 0;
      }
    } else if (player.dx < 0) {
      if ((cell && !cellright) || (celldown && !celldiag && ny)) {
        player.x = t2p(tx + 1); // clamp the x position to avoid moving into the platform we just hit
        player.dx = 0; // stop horizontal velocity
        nx = 0;
      }
    }
    player.falling = !(celldown || (nx && celldiag)); //falling if there is nether are true
  },
  render: dt => {
    //console.log(width, height);
    ctx.clearRect(0, 0, width, height);
    for (var h = 0; h < MAP.th; h++) {
      for (var w = 0; w < MAP.tw; w++) {
        ctx.fillStyle = COLORS[tcell(w, h)];
        ctx.fillRect(w * TILE, h * TILE, TILE, TILE);
      }
    }
    ctx.fillStyle = COLOR.GREY;
    ctx.fillRect(player.x, player.y, TILE, TILE);
  }
});

